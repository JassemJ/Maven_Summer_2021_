package tutorial;

public class wrapperclass_ {
    public static void main(String[] args) {
        // wrapper class allows us to use primitive data types as references data types
        // reference data types can have useful methods
        // slower to access
        // can be used with collections (ex.ArrayList)

        /*
        primitive       wrapper

        boolean         Boolean
        char            Character
        int             Integer
        double          Double

        autoboxing = wrapper class can be set to primitive
          ex. Boolean x = true;
        unboxing = convert wrapper to primitive
          references can behave as primitives
         */
    }
}
